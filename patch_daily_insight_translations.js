let patchCount = 0;

for (const [lang, strings] of Object.entries(translations)) {
  if (content.includes(`// [dailyInsight:${lang}]`)) {
    console.log(`Skipping ${lang} (already patched)`);
    continue;
  }

  const block = `  dailyInsight: { // [dailyInsight:${lang}]
    title: ${JSON.stringify(strings.title)},
    loading: ${JSON.stringify(strings.loading)},
    ayahTag: ${JSON.stringify(strings.ayahTag)},
    hadithTag: ${JSON.stringify(strings.hadithTag)},
    tapToChooseTrans: ${JSON.stringify(strings.tapToChooseTrans)},
    loadingTranslation: ${JSON.stringify(strings.loadingTranslation)},
    quranSource: ${JSON.stringify(strings.quranSource)},
    hadithSource: ${JSON.stringify(strings.hadithSource)},
    openReference: ${JSON.stringify(strings.openReference)},
  },\n`;

  // Match the language constant and insert before its closing };
  const langConstRegex = new RegExp(`(const ${lang} = \\{[\\s\\S]*?)(\\n\\};)`, '');
  if (langConstRegex.test(content)) {
    content = content.replace(langConstRegex, (match, body, closing) => {
      return body + '\n' + block + closing;
    });
    patchCount++;
    console.log(`Patched: ${lang}`);
  } else {
    console.log(`Could not find: ${lang}`);
  }
}

fs.writeFileSync(FILE_PATH, content, 'utf8');
console.log(`\nDone! Patched ${patchCount} languages.`);
